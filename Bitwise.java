package firstproject;
import java.io.*;

public class Bitwise {
		  public static void main(String[] args) throws IOException {
		    int a = 20,    b = 15;
		   
		System.out.println("The result after performing num1&num2 " + (a & b));
		    System.out.println("The result after performing num1|num2 " + (a | b));
		System.out.println("The result after performing num1^num2 " + (a ^ b));
		    System.out.println("The result after performing ~num2 " + (~a));
		  }


	}
